function add(a, b) {
  return a + b;
}

function subsctract(a, b) {
  return b - a;
}

function multiply(a, b) {
  return a * b;
}

function divide(a, b) {
  return a / b;
}

function increment(n) {
  return n++;
}

function decrement(n) {
  return n--;
}

function makeInt(n) {
  return parseInt(n);
}

function makeInt(n) {
  return parseInt(n, 10);
}
